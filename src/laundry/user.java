    private void showDetailUser(int idUser) {
        
        try {
            String sql = "SELECT id_user, username, role, password FROM user WHERE id_user=?";
            PreparedStatement st = connection.prepareStatement(sql);
            st.setInt(1, idUser);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                txtIdUser.setText(rs.getString("id_user"));
                txtUsername.setText(rs.getString("username"));
                txtRole.setText(rs.getString("role"));
                txtOldPassword.setText(rs.getString("password"));
                
            } 
            
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    
