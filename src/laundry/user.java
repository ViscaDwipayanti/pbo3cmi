    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here: 
        String oldPassword = txtOldPassword.getText();
        String newPassword = new String(txtNewPassword.getPassword());
        String confirmPassword = new String(txtConfirmPassword.getPassword());
        String username = txtUsername.getText();
        String currentPassword = null;

     // Verifikasi password lama
        try {
            String sqlCheck = "SELECT password FROM user WHERE id_user=?";
            PreparedStatement stCheck = connection.prepareStatement(sqlCheck);
            stCheck.setInt(1, UserSession.getIdUser());
            ResultSet rsCheck = stCheck.executeQuery();

            if (rsCheck.next()) {
                currentPassword = rsCheck.getString("password");
                if (!oldPassword.equals(currentPassword)) {
                    JOptionPane.showMessageDialog(this, "Password lama tidak sesuai!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            // Jika password baru diisi, lakukan validasi
            String passwordToUpdate = currentPassword; // Default gunakan password lama
            if (!newPassword.isEmpty()) {
                if (!newPassword.equals(confirmPassword)) {
                    JOptionPane.showMessageDialog(this, "Password baru dan konfirmasi tidak cocok!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                passwordToUpdate = newPassword; // Ganti dengan password baru
            }

            // Update password (jika ada) dan username
            String sqlUpdate = "UPDATE user SET password=?, username=? WHERE id_user=?";
            PreparedStatement stUpdate = connection.prepareStatement(sqlUpdate);
            stUpdate.setString(1, passwordToUpdate);
            stUpdate.setString(2, username);
            stUpdate.setInt(3, UserSession.getIdUser());

            int rowsUpdated = stUpdate.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Berhasil disimpan!", "Success", JOptionPane.INFORMATION_MESSAGE);
                dashboard dashboard = new dashboard();
                dashboard.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal update data!", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnSimpanActionPerformed

